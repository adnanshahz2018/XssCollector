function collector(context_regex){
	var context_value = ""	
	var line = document.documentElement.innerHTML.match(context_regex)
	if (line == null || line == undefined || line == "undefined" )
		{context_value = "" }
	else
		{context_value = line + ","}

	return context_value;
} 
